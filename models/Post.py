class Post():

    def __init__(self, content, publish_date, last_modify, like_count, dislike_count, who_likes, who_dislikes, author, board):
        self.content = content
        self.publish_date = publish_date
        self.last_modify = last_modify
        self.like_count = like_count
        self.dislike_count = dislike_count
        self.who_likes = who_likes
        self.who_dislikes = who_dislikes
        self.author = author
        self.board = board
